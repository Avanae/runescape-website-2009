                                                    <a name="Tutorial Island"></a><br class="clear">
                                                    
                                                    <div class="headings">
                                                        <span class="headertitleleft"><span class="headertitleright">Tutorial Island</span></span>
                                                    </div>
                                                    
                                                    <br>
  
                                                    <p>
                                                        Once you have created your account, you will spawn at the start of Tutorial Island.
                                                        You can skip the tutorial by talking to Skippie. I know many of you have completed Tutorial Island so many times that you can't fathom the thought of doing it again. 
                                                        Do keep in mind that the default XP Rate is 5, so if you're a skiller you will want to skip the tutorial.
                                                        
                                                        <br>
                                                        <br>

                                                        <b>Note: In MithScape we have four different XP rates and you will have the option of changing your XP rate by speaking with the Wizard at the end of the tutorial, but keep in mind you can only change it once, and before reaching a total level of 50.</b>
                                                        
                                                        <br>
                                                        <br>
                                                        
                                                        The current XP rates are as follows:
                                                        
                                                        <br>
                                                        <br>

                                                        <ul>
                                                            <li><b>No multiplier</b> - The authentic XP rate that many desire for the full experience.</li>
                                                            <li><b>x2.5 Multiplier</b> - For the players who want the full experience with a little less grinding.</li>
                                                            <li><b>x5 Multiplier</b> - The default MithScape XP rate.</li>
                                                            <li><b>x10 Multiplier</b> - A new XP rate introduced with the Hardcore Iron Mode and exclusive to this mode.</li>
                                                        </ul>

                                                        <br>
                                                        <br>
                                                        
                                                        <b>Note: In MithScape we have three different Iron Modes and you will have the option of selecting your Iron Man Mode by speaking with the Wizard at the end of the tutorial, but keep in mind you can only change it once. For more information you can read the <a href="kbase/ironmode" style="color: #9c6802;">Iron Mode article.</a></b>
                                                        
                                                        <br>
                                                        <br>
                                                        
                                                        The current available Iron Modes:
                                                        
                                                        <br>
                                                        <br>

                                                        <ul>
                                                            <li><b>Standard</b> - This account mode is the basic of the three account types. Ironmen & Ironwomen are unable to trade other players so they are completely self-sufficient. They also can not use lootshare whatsoever which means they are still stuck collecting everything on their own.</li>
                                                            <br>
                                                            <li><b>Ultimate</b> - Like Standard Iron Mode, the only other difference is that Ultimate Ironmen & Ironwomen can't bank items. However, they can note items on bank booths and unnote the items.</li>
                                                            <br>
                                                            <li><b>Permadeath</b> - A Permadeath account has all of the above restrictions, with one addition: when you die to a non-safe in-game activity, your account will be reset and sent back to Tutorial Island.</li>
                                                        </ul>
                                                    </p>
                                                    
